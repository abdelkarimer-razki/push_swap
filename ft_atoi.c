#include "push_swap.h"

void	eror(int k)
{
	if (k > 2147483647 || k < -2147483648)
	{
		write(2, "error\n", 6);
		exit(0);
	}
}

int	ft_atoi(const char *str)
{
	char		*c;
	size_t		k;
	size_t		m;

	c = (char *)str;
	m = 1;
	k = 0;
	while (*c == ' ' || *c == '\n'
		|| *c == '\t' || *c == '\r'
		|| *c == '\f' || *c == '\v' )
		c++;
	if (*c == '-' || *c == '+')
	{
		if (*c == '-')
			m = m * -1;
		c++;
	}
	while (*c >= '0' && *c <= '9')
	{
		k = (*c - 48) + k * 10;
		c++;
	}
	eror((int)k * m);
	return ((int)(k * m));
}
